    private void mapaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mapaMouseClicked
        // TODO add your handling code here:
      int  myszX=evt.getX();
          int myszY=evt.getY();
          int wybranyX=myszX/rozmiarX;
          int wybranyY=myszY/rozmiarY;
          if(Kształty.getSelectedItem()=="Pojedynczy punkt")
          {
          if(automat.stany[wybranyX][wybranyY]==1)
          {
          automat.stany[wybranyX][wybranyY]=0;
          g.setColor(Color.black);
          g.fillRect(wybranyX*rozmiarX, wybranyY*rozmiarY, rozmiarX, rozmiarY);
          }
          else
          {
           automat.stany[wybranyX][wybranyY]=1;
          g.setColor(Color.green);
          g.fillRect(wybranyX*rozmiarX, wybranyY*rozmiarY, rozmiarX, rozmiarY);
          }
          }
          else
          {
               for(int i=0;i<template.size();i++) {
            Pointer point=template.get(i);
            automat.stany[wybranyX+point.x][wybranyY+point.y]=1;
            przerysuj(wybranyX+point.x, wybranyY+point.y, Color.green);
        }
          }
        
    }//GEN-LAST:event_mapaMouseClicked

