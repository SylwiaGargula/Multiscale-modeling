private void loadTemplate(String s) {

        template.removeAll(template);
        try {
            BufferedReader br = new BufferedReader(new FileReader(s));
            String sCurrentLine;
            while ((sCurrentLine = br.readLine()) != null) {
                String[] temp = sCurrentLine.split("\\s+");
               template.add(new Pointer(Integer.valueOf(temp[0]),Integer.valueOf(temp[1])));
            }

        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
